// 중국어 병원명 자동 번역
export function translateHospitalName(hospitalName) {
  if (!hospitalName) return hospitalName
  
  // 정확한 매칭
  if (seoulHospitals[hospitalName]) {
    return seoulHospitals[hospitalName]
  }
  
  // 접미사 처리 ("医院", "病院" 등)
  const variations = [
    hospitalName + '医院',
    hospitalName + '병원',
    hospitalName + '病院',
    hospitalName.replace('医院', ''),
    hospitalName.replace('병원', ''),
    hospitalName.replace('病院', ''),
    hospitalName.replace('医', ''),
    hospitalName + '의료원',
    hospitalName + '센터'
  ]
  
  for (const variation of variations) {
    if (seoulHospitals[variation]) {
      return seoulHospitals[variation]
    }
  }
  
  return hospitalName
}

