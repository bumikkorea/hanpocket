/**
 * Search places by query in any language (ko/zh/en/ja)
 * Returns top N matches with Korean name for Kakao Map lookup
 * Extended places (with category field) are prioritized
 */
export function searchLocalPlaces(query, limit = 7) {
  if (!query || query.length < 1) return []
  const q = query.toLowerCase().trim()
  const exact = []
  const startsWith = []
  const contains = []
  for (const p of HANPOCKET_PLACE_DB) {
    const fields = [
      p.ko.toLowerCase(),
      p.zh.toLowerCase(),
      p.en.toLowerCase(),
      (p.ja || '').toLowerCase(),
    ]
    if (fields.some(f => f && f === q)) { exact.push(p); continue }
    if (fields.some(f => f && f.startsWith(q))) { startsWith.push(p); continue }
    if (fields.some(f => f && f.includes(q))) { contains.push(p); continue }
  }
  // Prioritize extended places (with category)
  const sortByExtended = (arr) => arr.sort((a, b) => (b.category ? 1 : 0) - (a.category ? 1 : 0))
  return [...sortByExtended(exact), ...sortByExtended(startsWith), ...sortByExtended(contains)].slice(0, limit)
}
