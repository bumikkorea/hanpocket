// 중국어 대학명 자동 번역
export function translateUniversityName(universityName) {
  if (!universityName) return universityName
  
  // 정확한 매칭
  if (seoulUniversities[universityName]) {
    return seoulUniversities[universityName]
  }
  
  // "大学", "大" 등 접미사 처리
  const variations = [
    universityName + '大学',
    universityName + '大学校', 
    universityName + '大',
    universityName.replace('大学', ''),
    universityName.replace('大学校', ''),
    universityName.replace('大', '')
  ]
  
  for (const variation of variations) {
    if (seoulUniversities[variation]) {
      return seoulUniversities[variation]
    }
  }
  
  return universityName
}

