// 중국어 지하철역명 자동 번역 함수
export function translateSubwayStation(stationName) {
  if (!stationName) return stationName
  
  // 정확한 매칭
  if (seoulSubwayStations[stationName]) {
    return seoulSubwayStations[stationName]
  }
  
  // "站" 제거 후 매칭
  const withoutSuffix = stationName.replace('站', '')
  if (seoulSubwayStations[withoutSuffix + '站']) {
    return seoulSubwayStations[withoutSuffix + '站']
  }
  
  return stationName
}

