// 중국어 공공시설명 자동 번역
export function translatePublicFacilityName(facilityName) {
  if (!facilityName) return facilityName
  
  // 정확한 매칭
  if (seoulPublicFacilities[facilityName]) {
    return seoulPublicFacilities[facilityName]
  }
  
  // 접미사 처리
  const variations = [
    facilityName + '구청',
    facilityName + '区厅',
    facilityName + '住민센터',  
    facilityName + '주민센터',
    facilityName + '박물관',
    facilityName + '博物馆',
    facilityName + '도서관',
    facilityName + '图书馆',
    facilityName + '고등학교',
    facilityName + '高中',
    facilityName + '公园',
    facilityName + '공원',
    facilityName.replace('区厅', '구청'),
    facilityName.replace('博物馆', '박물관'),
    facilityName.replace('图书馆', '도서관'),
    facilityName.replace('高中', '고등학교'),
    facilityName.replace('公园', '공원')
  ]
  
  for (const variation of variations) {
    if (seoulPublicFacilities[variation]) {
      return seoulPublicFacilities[variation]
    }
  }
  
  return facilityName
}

