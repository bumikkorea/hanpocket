// 중국어 쇼핑몰명 자동 번역
export function translateShoppingMallName(mallName) {
  if (!mallName) return mallName
  
  // 정확한 매칭
  if (seoulShoppingMalls[mallName]) {
    return seoulShoppingMalls[mallName]
  }
  
  // 부분 매칭 (긴 키워드부터)
  const sortedKeys = Object.keys(seoulShoppingMalls).sort((a, b) => b.length - a.length)
  
  for (const key of sortedKeys) {
    if (mallName.includes(key)) {
      return seoulShoppingMalls[key]
    }
  }
  
  return mallName
}

